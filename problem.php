<h1 style="color:#900">The Problem Statement</h1>
<div style="text-align:left">
<p>#1.&nbsp;Twitter-Timeline Challenge<br />
Please create a  small PHP-script to accomplish following parts:<br />
<strong>Part-1: User Timeline</strong></p>
<ol>
  <li>Start =&gt; User  visit your script page.</li>
  <li>He will be asked to  connect using his Twitter account using&nbsp;Twitter Auth.</li>
  <li>After  authentication, your script will pull latest 10 tweets form his &quot;home&quot;  timeline.</li>
  <li>10 tweets will be  displayed using a jQuery-slideshow.</li>
  <li>There will be a  download button below slideshow. When user click download button, a PDF  with&nbsp;<em>&quot;10 tweets&quot;</em>&nbsp;content from slidehow will be downloaded  to users computer.</li>
</ol>
<p><strong>Part-2: Followers Timeline</strong></p>
<ol>
  <li>Below  jQuery-slideshow&nbsp;<em>(in step#4 from part-1)</em>, display  list 10 followers (you can take any 10 random followers).</li>
  <li>Also, display a  search followers box. Add auto-suggest support. That means as soon as user starts  typing, his followers will start showing.</li>
  <li>When user will  click on a follower name, 10 tweets from that follower's user-timeline will be  displayed in same jQuery-slider, without page refresh (use AJAX).</li>
</ol>
<p>Both parts are  must. Feel free to add any extra functionality you want to make it better!</p>
